def clean_city_name(df, column_name):
    # Daftar karakter beraksen bahasa Portugis/Brazil
    accents    = "áàâãäåéèêëíìîïóòôõöúùûüçñºª"
    # Huruf penggantinya (tanpa aksen)
    no_accents = "aaaaaaeeeeiiiiooooouuuucnoa"

    return df.withColumn(column_name, F.lower(F.col(column_name))) \
             .withColumn(column_name, F.translate(F.col(column_name), accents, no_accents)) \
             .withColumn(column_name, F.regexp_replace(F.col(column_name), "[^a-z0-9 ]", "")) \
             .withColumn(column_name, F.regexp_replace(F.col(column_name), " +", " ")) \
             .withColumn(column_name, F.trim(F.col(column_name)))

