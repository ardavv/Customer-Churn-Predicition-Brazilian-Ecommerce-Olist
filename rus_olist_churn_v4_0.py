# 1. Filter out rows that have any nulls AND are not 'delivered' or 'canceled'
# We want to keep rows where (no nulls) OR (has nulls AND order_status IN ('delivered', 'canceled'))
conditions = []
for c in orders.columns:
    is_null_cond = col(c).isNull()
    if isinstance(orders.schema[c].dataType, (DoubleType, FloatType)):
        conditions.append(is_null_cond | isnan(col(c)))
    else:
        conditions.append(is_null_cond)

# Combine all conditions with OR to find rows with any null/NaN
any_null_row_condition = reduce(lambda a, b: a | b, conditions) if conditions else F.lit(False)

orders = orders.filter((~any_null_row_condition) | (F.col('order_status').isin('delivered', 'canceled')))

# 2. Impute nulls in 'order_approved_at' using median 'purchased_approved' duration (in seconds, so we multiply by 3600 since median_purchased_approved is in hours)
median_purchased_approved = orders.agg(F.expr("percentile_approx(purchased_approved, 0.5)")).collect()[0][0]
orders = orders.withColumn(
    'order_approved_at',
    F.when(
        F.col('order_approved_at').isNull(),
        F.from_unixtime(F.unix_timestamp(F.col('order_purchase_timestamp')) + F.lit(median_purchased_approved * 3600))
    ).otherwise(F.col('order_approved_at'))
)

# 3. Impute nulls in 'order_delivered_carrier_date' using median 'approved_carrier' duration
median_approved_carrier = orders.agg(F.expr("percentile_approx(approved_carrier, 0.5)")).collect()[0][0]
orders = orders.withColumn(
    'order_delivered_carrier_date',
    F.when(
        F.col('order_delivered_carrier_date').isNull(),
        F.date_add(F.col('order_approved_at'), F.lit(median_approved_carrier))
    ).otherwise(F.col('order_delivered_carrier_date'))
)

# 4. Impute nulls in 'order_delivered_customer_date' using median 'carrier_delivered' duration
median_carrier_delivered = orders.agg(F.expr("percentile_approx(carrier_delivered, 0.5)")).collect()[0][0]
