# 3. Fungsi pembantu untuk melakukan cross-validation dan mengambil rata-ratanya
def evaluate_model_kfold(model, X, y, cv, scoring):
    # Lakukan K-Fold Cross Validation (n_jobs=-1 agar menggunakan semua core CPU/lebih cepat)
    cv_results = cross_validate(model, X, y, cv=cv, scoring=scoring, n_jobs=-1)

    # Ambil rata-rata (mean) dari hasil 5 folds untuk masing-masing metrik
    mean_results = {
        'Accuracy': np.mean(cv_results['test_accuracy']),
        'Precision': np.mean(cv_results['test_precision_macro']),
        'Recall': np.mean(cv_results['test_recall_macro']),
        'F1-Score': np.mean(cv_results['test_f1_macro']),
        'ROC-AUC': np.mean(cv_results['test_roc_auc'])
    }
    return mean_results

